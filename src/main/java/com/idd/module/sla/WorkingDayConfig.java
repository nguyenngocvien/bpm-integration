package com.idd.module.sla;

public class WorkingDayConfig {

    private int startMorning;
    private int endMorning;
    private int startAfternoon;
    private int endAfternoon;

    public int getStartMorning() {
        return startMorning;
    }

    public void setStartMorning(int startMorning) {
        this.startMorning = startMorning;
    }

    public int getEndMorning() {
        return endMorning;
    }

    public void setEndMorning(int endMorning) {
        this.endMorning = endMorning;
    }

    public int getStartAfternoon() {
        return startAfternoon;
    }

    public void setStartAfternoon(int startAfternoon) {
        this.startAfternoon = startAfternoon;
    }

    public int getEndAfternoon() {
        return endAfternoon;
    }

    public void setEndAfternoon(int endAfternoon) {
        this.endAfternoon = endAfternoon;
    }
}
